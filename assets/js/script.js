
        function bootstrap() {
            var counter = 0;
            var i = setInterval(function() {
                let y = counter + "%";
                $("#bootstrap").css({
                    "width": y
                });

                $("#bootstrap").html(y);
                counter++;
                if (counter === 76) {
                    clearInterval(i);
                }
            }, 150);

        }

